    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
        ]);

        $data = $request->all();
        $data['uuid'] = substr(md5(uniqid(rand(), true)), 0, 8);

        try {
            People::create($data);
            return redirect()->route('show', $data['uuid']);
        } catch (\Throwable $th) {
            return redirect()->route('show', $data['uuid'])->with('error', 'Gagal membuat akun.');
        }
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($uuid)
    {
        $people = People::where('uuid', $uuid)->first();
        $people->setRelation('messages', $people->messages()->paginate(5));

        return view('pages.user.home.show', compact('people'));
    }

